        <p class="citation-intro">
            « Every pixel has a memory, and every child who plays remembers. » — Hideo Kojima

        </p>
        <p>
            Depuis tout petit, j’ai toujours perçu le jeu vidéo comme une porte dérobée vers un autre monde. 
            Devant l’écran, les pixels vibraient comme des lucioles enfermées dans la nuit, dessinant sous mes 
            yeux des paysages mouvants que mes mains pouvaient toucher. C’était un émerveillement premier — celui 
            de la puissance du geste. Le simple fait d’effleurer un joystick, de guider un avatar, me donnait 
            l’impression d’habiter un rêve, de le modeler en direct. En cela, le jeu n’était pas une distraction, 
            mais un rite d’initiation. Comme dans les mythes anciens où le héros quitte son village pour affronter 
            l’inconnu, chaque partie représentait un passage, une traversée. On y entrait enfant, on en ressortait 
            différent, transformé par les épreuves, parfois marqué par une mélancolie discrète — celle du retour à 
            la réalité. Joseph Campbell parlait de ce « voyage du héros » où l’aventure devient une métaphore de la 
            croissance intérieure : le joueur, tout comme le héros mythique, s’éveille à lui-même à travers le monde 
            qu’il traverse.
        </p>

